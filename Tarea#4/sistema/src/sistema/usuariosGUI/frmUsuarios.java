    //Método para mostrar los datos en forma de registro en mi tabla de forma automatica.
    public void mostrarDataos(){
        //Preprando mis registros en mi tabla...
        while(modelo.getRowCount()>0){
            modelo.removeRow(0);
        }
        //Estableciendo conexión en mi base de datos.
        conexion objConexion= new conexion();
        //Try catch para el manejo de posibles errores.
          try {
            //Realizando consulta para traer todos los registros que esten en mi tabla usuario de mi base de datos sistema.
            ResultSet resultado = objConexion.consultarRegistros("SELECT * FROM usuario");
            //Tomando todos los registros y guardandolo en un object para luego mostarlos en mi tabla del programa...
            while (resultado.next()) {
                System.out.println(resultado.getString("idusuario"));
                System.out.println(resultado.getString("usuario"));
                System.out.println(resultado.getString("nombre"));
                System.out.println(resultado.getString("apellido"));
                System.out.println(resultado.getString("telefono"));
                System.out.println(resultado.getString("Correo_Electronico"));
                Object[] oUsuario={resultado.getString("idusuario"),resultado.getString("usuario"),resultado.getString("nombre"),resultado.getString("apellido"),resultado.getString("telefono"),resultado.getString("Correo_Electronico") };
                modelo.addRow(oUsuario);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
        
        
    }
    //Método para establecerle los valores a mis atributos establecidos en mi clase usuarioBL, utilizando los datos que se ingresaron en mis txtFields
    public usuarioBL recuperarDatosGUI(){
        usuarioBL oUsuarios= new usuarioBL();
        
        int id = (txtID.getText().isEmpty())?0: Integer.parseInt(txtID.getText());
        
        oUsuarios.setIdusuario(id);
        oUsuarios.setNombre(txtNombre.getText());
        oUsuarios.setApellido(txtApellido.getText());
        oUsuarios.setTelefono(txtTelefono.getText());
        oUsuarios.setCorreo_electronico(txtCorreo.getText());
        
        return oUsuarios;
    }
    //Método para limpiar los datos que contengan escritos mis txtFields.
